package shapes;

public class Reactangle {
    protected double length;
    protected double width;

    public Reactangle(double length, double width) {
        this.length = length;
        this.width = width;
    }

    public double getArea() {
        return length * width;
    }

    public double getPerimeter() {
        return 2 * (length + width);
    }
}
